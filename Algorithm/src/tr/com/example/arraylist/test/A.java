package tr.com.example.arraylist.test;

public class A {
	private int a;
	private String b;
	private short c;
	
	public A(int a, String b, short c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}

	public short getC() {
		return c;
	}

	public void setC(short c) {
		this.c = c;
	}
	
	
}
